# You need to use single quotes inside of double quotes
message = "One of Python's strengths is its diverse community."
print(message)

# use a if the line of code is too long to continue it on a second line
message2 = "Albert Einstein once said, 'A person who never made a mistake never tried \
anything new.'"
print(message2)

famous_person = "Albert Einstein"
message3 = f"{famous_person} once said, 'A person who never made a mistake never \
tried anything new.'"
print(message3)
